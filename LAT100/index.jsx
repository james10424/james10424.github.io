class Vocab extends React.Component {
    // arguments: units
    constructor(props) {
        super(props);
        this.state = {
            entries: [],
            words: {},
            checkbox: [],
            units: props.units,
            ans: "",
        };
        this.change_unit = () => {
            this.set_unit(Array.from(document.querySelectorAll(".unit")).filter(x => x.checked).map(x => x.value));
        };
        this.set_unit = (units) => {
            var w = Object.assign({}, ...units.map(u => all_units[u]));
            this.setState({
                units: units,
                cur: this.next_word(w),
                words: w,
                checkbox: Object.keys(all_units).map(u => 
                    <span class="unitbox"> 
                    <input class="unit" value={u} onClick={this.change_unit} type="checkbox" checked={units.includes(u)} /> unit {u.slice(1)} </span>)
            });
        };
        this.change_ans = (e) => {
            this.setState({ans: e.target.value});
        }
        // this.print_error = (correct, s) => {
        //     _correct = correct.split().join("").split(",");
        //     _s = s.split().join("").split(",");
        //     if (_s.length < _correct.length) {
        //         let tmp = [];
        //         for (let i = 0; i < _correct.length - _s.length; i++) {
        //             tmp.push("");
        //         }
        //         _s.push(...tmp);
        //     } // pad
        //     let sol = [];
        //     let hint = [];
        // };
        this.check_ans = () => {
            if (!this.state.cur) {
                this.setState({entries: [<div>Looks like you have finished with the words, click <button onClick={() => this.set_unit(this.state.units)}>here</button> to refresh</div>, ...this.state.entries], ans: ""});
                return;
            }
            var sol = this.state.words[this.state.cur];
            var ans = this.state.ans;
            var result;
            if (ans == sol) {
                result = <span style={{backgroundColor: "green"}}>Correct</span>;
                delete this.state.words[this.state.cur];
            }
            else {
                result = <span><span style={{backgroundColor: "red"}}>Wrong:</span> {sol}</span>;
            }
            ans = "You entered: " + ans;
            
            var next = this.next_word(this.state.words);
            this.setState({entries: [
            <div>
                <div>{this.state.cur}</div>
                <div>{ans} {result}</div>
                <br/>
            </div>, ...this.state.entries], cur: next, ans: ""});
        };
        this.next_word = (words) => {
            var w = Object.keys(words);
            return w[Math.floor(Math.random() * w.length)];
        };
    }

    componentDidMount() {
        this.set_unit(this.state.units);
    }

    render() {
        return (
        <div>
            <div style={{right: "0", position: "absolute", bottom: "0", padding: "1vh"}}> Checkout the sorce code for this website: <a target="_blank" href="https://github.com/james10424/LatinVocab">https://github.com/james10424/LatinVocab</a> </div>
            Select units here, the progress will reset everytime you checked. Please enter the exact form, with space and comma.
            <div>{this.state.checkbox}</div>
            <br/>
            <div>{Object.keys(this.state.words).length} left</div>
            You know the spiel:
            <div> {this.state.cur} </div>
            <br/>
            <input type="text" autocomplete="off" autocorrect="off" autocapitalize="off" spellcheck="false" value={this.state.ans} onKeyPress={e => e.key == "Enter" && this.check_ans()} onChange={this.change_ans}/>
            <input type="button" value="submit" onClick={this.check_ans} />
            <br/>
            <div>
                {this.state.entries}
            </div>
        </div>
        );
    }
}

ReactDOM.render(<Vocab units={["u15"]} />, document.getElementById("root"));